    {
        $contact=new Comment();
        $contact->post_id=request('post_id');
        $contact->comment_name=request('comment_name');
        $contact->comment_email=request('comment_email');
        $contact->comment_content=request('comment_content');
        $contact->created_at=now();
        $contact->updated_at=now();
        $contact->save();
        return back();
